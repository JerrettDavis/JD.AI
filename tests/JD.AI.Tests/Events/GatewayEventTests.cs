using FluentAssertions;
using JD.AI.Core.Events;

namespace JD.AI.Tests.Events;

public sealed class GatewayEventTests
{
    [Fact]
    public void Construction_SetsRequiredProperties()
    {
        var now = DateTimeOffset.UtcNow;
        var evt = new GatewayEvent("tool.invoked", "session-1", now);
        evt.EventType.Should().Be("tool.invoked");
        evt.SourceId.Should().Be("session-1");
        evt.Timestamp.Should().Be(now);
        evt.Payload.Should().BeNull();
    }

    [Fact]
    public void Construction_WithPayload()
    {
        var payload = new { Tool = "read_file", Path = "/tmp" };
        var evt = new GatewayEvent("tool.invoked", "s1", DateTimeOffset.UtcNow, payload);
        evt.Payload.Should().NotBeNull();
    }

    [Fact]
    public void Id_AutoGenerated_NotEmpty()
    {
        var evt = new GatewayEvent("test", "src", DateTimeOffset.UtcNow);
        evt.Id.Should().NotBeNullOrWhiteSpace();
    }

    [Fact]
    public void Id_Unique_PerInstance()
    {
        var a = new GatewayEvent("test", "src", DateTimeOffset.UtcNow);
        var b = new GatewayEvent("test", "src", DateTimeOffset.UtcNow);
        a.Id.Should().NotBe(b.Id);
    }
}
